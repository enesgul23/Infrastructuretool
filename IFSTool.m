% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
Cvv=str2num(get(handles.edit1,'String'));
dR=str2num(get(handles.edit3,'String'));
Dgr=str2num(get(handles.edit2,'String'));
lambda=str2num(get(handles.edit4,'String'));

handles.Tum=[Cvv;Dgr;dR;lambda];

handles.Tum=mapminmax('apply',handles.Tum,handles.ps1);

handles.mlp=sim(handles.net,handles.Tum);

handles.mlp=mapminmax('reverse',handles.mlp,handles.ps3);

handles.alldata=[handles.Tum',handles.mlp];

handles.Froudparticle=m5ppredict(handles.model, handles.alldata);

set(handles.edit5,'String',handles.Froudparticle);

guidata(hObject, handles);


